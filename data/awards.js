const awardsTable = document.getElementById("awards-table");
let start = 0;
let end = 10;
function loadData() {
  let awardsResult = "";
  awardsData
    .slice(start, end)
    .map((item, index) => {
      awardsResult += `
    <tr>
      <td class="fw-bold">${item.id}</td>
      <td class="fw-bold">${item.SBU}</td>
      <td class="fw-bold"> ${item.category}</td>
      <td class="fw-bold"> ${item.winner}</td>
      <td class="view-awards-image text-center">
        <button class="button button-flex" data-index="${index}" data-bs-toggle="modal" data-bs-target="#imgModal">View <i class="fa-solid fa-eye"></i></button>
      </td>
    </tr>
  `;
    })
    .join("");
  awardsTable.innerHTML = awardsResult;
}
loadData();

const paginationButtons = document.querySelectorAll(".pagination-button");
paginationButtons.forEach((item) => {
  item.addEventListener("click", function () {
    start = this.getAttribute("data-start");
    end = this.getAttribute("data-end");
    loadData();
  });
});

