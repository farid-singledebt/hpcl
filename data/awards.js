const awardsTable = document.getElementById("awards-table");
let itemsPerPage = 10;
function loadData() {
  let awardsResult = "";
  awardsData
    .slice(0, itemsPerPage)
    .map((item, index) => {
      awardsResult += `
    <tr>
      <td>${index + 1}</td>
      <td>${item.SBU}</td>
      <td>${item.category}</td>
      <td>${item.winner}</td>
      <td class="view-image text-center" data-index="${index}" data-bs-toggle="modal" data-bs-target="#imgModal">
        <button class="button button-flex">View image <i class="fa-solid fa-eye"></i></button>
      </td>
    </tr>

  `;
    })
    .join("");
  awardsTable.innerHTML = awardsResult;
}
loadData();

const loadMore = document.getElementById("load-more");
loadMore.addEventListener("click", () => {
  if (itemsPerPage < awardsData.length) {
    itemsPerPage += 10;
  } else if (itemsPerPage > awardsData.length) {
    itemsPerPage = 10;
    loadMore.textContent = "Load more";
  }
  if (itemsPerPage >= awardsData.length) {
    loadMore.textContent = "Reset";
  }
  loadData();
});

document.querySelectorAll(".view-image").forEach((item) => {
  item.addEventListener("click", function () {
    const index = this.getAttribute("data-index");

    const $owl = $("#modal-carousel");
    if ($owl.hasClass("owl-loaded")) {
      $owl.trigger("destroy.owl.carousel"); // Destroy current carousel
      $owl.removeClass("owl-loaded"); // Remove owl-loaded class
      $owl.find(".owl-stage-outer").children().unwrap(); // Remove wrapper generated by OwlCarousel
    }

    // Generate the Owl Carousel items with images
    let carouselItems = awardsData
      .map(
        (award, i) => `
      <div class="item">
        <img src="${award.img}" alt="Image ${i + 1}" />
      </div>
    `
      )
      .join("");

    // Insert the carousel items into the modal
    document.getElementById("modal-carousel").innerHTML = carouselItems;

    // Re-initialize the Owl Carousel
    $("#modal-carousel").owlCarousel({
      items: 1,
      loop: true,
      nav: true,
      dots: false,
      startPosition: index,
    });
  });
});
